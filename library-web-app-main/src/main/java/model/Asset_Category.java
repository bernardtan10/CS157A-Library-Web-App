package model;

public class Asset_Category {
	String ac_id;
	String ac_name;
	String ac_location;
	public String getAsset_id() {
		return ac_id;
	}
	public void setAsset_id(String ac_id) {
		this.ac_id = ac_id;
	}
	public String getAsset_name() {
		return ac_name;
	}
	public void setAsset_name(String ac_name) {
		this.ac_name = ac_name;
	}
	public String getAsset_location() {
		return ac_location;
	}
	public void setAsset_location(String ac_location) {
		this.ac_location = ac_location;
	}
}